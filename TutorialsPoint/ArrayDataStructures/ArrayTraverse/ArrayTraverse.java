public class ArrayTraverse{
  public static void main(String[] args){
    private int[] Num_Array = {55,94,856,7,45,4556,96,8,6};

    System.out.println("All array elements:");
    for(int index = 0; index < Num_Array.length; index++){
      System.out.println("Array element at index " + index + ": " + Num_Array[index]);
    }

  }
}
